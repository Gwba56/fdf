#include "libft.h"

int	ft_iscolor(char *str)
{
	int	i;

	i = 2;
	if (str[0] == '0' && (str[1] == 'x' || str[1] == 'X'))
	{
		while (ft_ishexa(str[i] == 1))
			i++;
		if (i == 8)
			return (1);
	}
	return (0);
}
